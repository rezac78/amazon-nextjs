import "./globals.css";
import Footer from "../components/common/Footer";
import MountWrapper from "../components/common/MountWrapper";
import {Toaster} from "@/components/ui/sonner";
import {categories, locales} from "@/utils/api/home/fetchhome";
import HeaderWrapper from "@/components/common/HeaderWrapper";
import {getAuthToken} from "@/utils/checkCookies";
import {cookies} from "next/headers";
export const metadata = {
 title: "Create Next App",
 description: "Generated by create next app",
};
export default async function RootLayout({children}: {children: React.ReactNode}) {
 const cookieStore = await cookies();
 const lang = cookieStore.get("lang")?.value || "fa";
 const dir = lang === "fa" || lang === "ar" ? "rtl" : "ltr";
 const token = await getAuthToken();
 const locale = await locales();
 const categorie = await categories(lang);
 return (
  <html dir={dir} lang={lang}>
   <head>
    <link rel="preload" as="image" href="/pages/home/slide1.webp" />
   </head>
   <body className={`font-sansv flex flex-col min-h-dvh`}>
    <HeaderWrapper isLogin={!!token} categorie={categorie} token={token ?? ""} locale={locale} lang={lang} />
    <main className="flex flex-grow w-full">
     <MountWrapper>{children}</MountWrapper>
    </main>
    <Toaster />
    <Footer />
   </body>
  </html>
 );
}
